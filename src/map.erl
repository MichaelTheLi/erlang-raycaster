convertMapToDict(Map) ->
  Dict = dict:new(),
  FunY = fun (Cell, {X, Y, Dict}) ->
    NewDict = dict:store({X, Y}, Cell, Dict),
    {X, Y+1, NewDict}
         end,
  FunX = fun (Column, {X, Y, Dict}) ->
    {NX, NY, NewDict} = lists:foldl(FunY, {X, Y, Dict}, Column),
    {NX+1, Y, NewDict}
         end,
  {X, Y, NewDict} = lists:foldl(FunX, {1, 1, Dict}, Map),
  NewDict.

